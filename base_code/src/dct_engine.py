import math

# Precompute 1D IDCT coefficients as tuples for fast read access.
#   coeffs[freq][pos] = 0.5 * alpha(freq) * cos((2*pos+1) * freq * pi / 16)
#   alpha(0) = 1/sqrt(2), alpha(f!=0) = 1
def _build_coeffs():
    rows = []
    for f in range(8):
        af = 1.0 / math.sqrt(2.0) if f == 0 else 1.0
        rows.append(tuple(
            0.5 * af * math.cos((2.0 * p + 1.0) * f * math.pi / 16.0)
            for p in range(8)
        ))
    return tuple(rows)

_C = _build_coeffs()
# Individual rows for direct local binding (avoid global list-of-lists overhead)
_C0, _C1, _C2, _C3, _C4, _C5, _C6, _C7 = _C


