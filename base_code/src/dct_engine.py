    t20 = s0*c0[0] + s1*c1[0] + s2*c2[0] + s3*c3[0] + s4*c4[0] + s5*c5[0] + s6*c6[0] + s7*c7[0]
    t21 = s0*c0[1] + s1*c1[1] + s2*c2[1] + s3*c3[1] + s4*c4[1] + s5*c5[1] + s6*c6[1] + s7*c7[1]
