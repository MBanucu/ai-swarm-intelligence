    # Row 4
    s0, s1, s2, s3, s4, s5, s6, s7 = matrix[4]
    t40 = s0*c0[0] + s1*c1[0] + s2*c2[0] + s3*c3[0] + s4*c4[0] + s5*c5[0] + s6*c6[0] + s7*c7[0]
    t41 = s0*c0[1] + s1*c1[1] + s2*c2[1] + s3*c3[1] + s4*c4[1] + s5*c5[1] + s6*c6[1] + s7*c7[1]
    t42 = s0*c0[2] + s1*c1[2] + s2*c2[2] + s3*c3[2] + s4*c4[2] + s5*c5[2] + s6*c6[2] + s7*c7[2]
    t43 = s0*c0[3] + s1*c1[3] + s2*c2[3] + s3*c3[3] + s4*c4[3] + s5*c5[3] + s6*c6[3] + s7*c7[3]
    t44 = s0*c0[4] + s1*c1[4] + s2*c2[4] + s3*c3[4] + s4*c4[4] + s5*c5[4] + s6*c6[4] + s7*c7[4]
    t45 = s0*c0[5] + s1*c1[5] + s2*c2[5] + s3*c3[5] + s4*c4[5] + s5*c5[5] + s6*c6[5] + s7*c7[5]
    t46 = s0*c0[6] + s1*c1[6] + s2*c2[6] + s3*c3[6] + s4*c4[6] + s5*c5[6] + s6*c6[6] + s7*c7[6]
    t47 = s0*c0[7] + s1*c1[7] + s2*c2[7] + s3*c3[7] + s4*c4[7] + s5*c5[7] + s6*c6[7] + s7*c7[7]

    # Row 5
    s0, s1, s2, s3, s4, s5, s6, s7 = matrix[5]
    t50 = s0*c0[0] + s1*c1[0] + s2*c2[0] + s3*c3[0] + s4*c4[0] + s5*c5[0] + s6*c6[0] + s7*c7[0]
    t51 = s0*c0[1] + s1*c1[1] + s2*c2[1] + s3*c3[1] + s4*c4[1] + s5*c5[1] + s6*c6[1] + s7*c7[1]
    t52 = s0*c0[2] + s1*c1[2] + s2*c2[2] + s3*c3[2] + s4*c4[2] + s5*c5[2] + s6*c6[2] + s7*c7[2]
