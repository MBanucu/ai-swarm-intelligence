    # Row 6
    s0, s1, s2, s3, s4, s5, s6, s7 = matrix[6]
    t60 = s0*c0[0] + s1*c1[0] + s2*c2[0] + s3*c3[0] + s4*c4[0] + s5*c5[0] + s6*c6[0] + s7*c7[0]
    t61 = s0*c0[1] + s1*c1[1] + s2*c2[1] + s3*c3[1] + s4*c4[1] + s5*c5[1] + s6*c6[1] + s7*c7[1]
    t62 = s0*c0[2] + s1*c1[2] + s2*c2[2] + s3*c3[2] + s4*c4[2] + s5*c5[2] + s6*c6[2] + s7*c7[2]
    t63 = s0*c0[3] + s1*c1[3] + s2*c2[3] + s3*c3[3] + s4*c4[3] + s5*c5[3] + s6*c6[3] + s7*c7[3]
