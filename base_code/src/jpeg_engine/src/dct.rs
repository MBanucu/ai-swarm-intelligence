// ──────────────────────────────────────────────────────
// 1‑D FDCT — even/odd decomposition (row pass)
//
// a[x] = f[x] + f[7-x]  (sum pairs)
// d[x] = f[x] - f[7-x]  (diff pairs)
//
// Even outputs: F[2k] = Σ a[x] · EVEN_ROW[k][x]
// Odd outputs:  F[2k+1] = Σ d[x] · ODD_ROW[k][x]
// ──────────────────────────────────────────────────────
#[inline(always)]
fn fdct_1d(src: &[f64; 8]) -> [f64; 8] {
    let s0 = src[0]; let s1 = src[1]; let s2 = src[2]; let s3 = src[3];
    let s4 = src[4]; let s5 = src[5]; let s6 = src[6]; let s7 = src[7];

    // Sum/diff pairs
    let a0 = s0 + s7; let a1 = s1 + s6; let a2 = s2 + s5; let a3 = s3 + s4;
    let d0 = s0 - s7; let d1 = s1 - s6; let d2 = s2 - s5; let d3 = s3 - s4;

    // Even part → F[0], F[2], F[4], F[6]
    let e0 = a0*EVEN_ROW[0][0] + a1*EVEN_ROW[0][1] + a2*EVEN_ROW[0][2] + a3*EVEN_ROW[0][3];
    let e1 = a0*EVEN_ROW[1][0] + a1*EVEN_ROW[1][1] + a2*EVEN_ROW[1][2] + a3*EVEN_ROW[1][3];
    let e2 = a0*EVEN_ROW[2][0] + a1*EVEN_ROW[2][1] + a2*EVEN_ROW[2][2] + a3*EVEN_ROW[2][3];
    let e3 = a0*EVEN_ROW[3][0] + a1*EVEN_ROW[3][1] + a2*EVEN_ROW[3][2] + a3*EVEN_ROW[3][3];

    // Odd part → F[1], F[3], F[5], F[7]
    let o0 = d0*ODD_ROW[0][0] + d1*ODD_ROW[0][1] + d2*ODD_ROW[0][2] + d3*ODD_ROW[0][3];
    let o1 = d0*ODD_ROW[1][0] + d1*ODD_ROW[1][1] + d2*ODD_ROW[1][2] + d3*ODD_ROW[1][3];
    let o2 = d0*ODD_ROW[2][0] + d1*ODD_ROW[2][1] + d2*ODD_ROW[2][2] + d3*ODD_ROW[2][3];
    let o3 = d0*ODD_ROW[3][0] + d1*ODD_ROW[3][1] + d2*ODD_ROW[3][2] + d3*ODD_ROW[3][3];

    [
        e0, o0,  // F[0], F[1]
        e1, o1,  // F[2], F[3]
        e2, o2,  // F[4], F[5]
        e3, o3,  // F[6], F[7]
    ]
}

