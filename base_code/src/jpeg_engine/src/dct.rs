/// Forward DCT — separable row‑column implementation
/// Complexity: 8·64 + 8·64 = 1024 multiply‑adds  (vs 4096 for naive)
pub fn fdct_2d(block: &mut [f64; 64]) {
    let mut tmp = [0.0f64; 64];

    // 1) 1‑D DCT on every row (using unscaled row‑pass matrix)
    let mut off = 0;
    while off < 64 {
        let row = [
            block[off], block[off+1], block[off+2], block[off+3],
            block[off+4], block[off+5], block[off+6], block[off+7],
        ];
        let d = fdct_1d(&row);
        tmp[off]     = d[0];
        tmp[off+1]   = d[1];
        tmp[off+2]   = d[2];
        tmp[off+3]   = d[3];
        tmp[off+4]   = d[4];
        tmp[off+5]   = d[5];
        tmp[off+6]   = d[6];
        tmp[off+7]   = d[7];
        off += 8;
    }

    // 2) 1‑D DCT on every column, using column‑pass matrix
    //    which includes the 0.25 total scaling (fused — no extra multiply).
    for u in 0..8 {
        let col = [
            tmp[u], tmp[8 + u], tmp[16 + u], tmp[24 + u],
            tmp[32 + u], tmp[40 + u], tmp[48 + u], tmp[56 + u],
        ];
        let d = fdct_1d_col(&col);
        block[u]      = d[0];
        block[8 + u]  = d[1];
        block[16 + u] = d[2];
        block[24 + u] = d[3];
        block[32 + u] = d[4];
        block[40 + u] = d[5];
        block[48 + u] = d[6];
        block[56 + u] = d[7];
    }
}

/// Naïve 2‑D IDCT (used only in tests for roundtrip verification)
pub fn idct_2d_naive(block: &mut [f64; 64]) {
    let mut temp = [0.0f64; 64];

    for x in 0..8 {
        for y in 0..8 {
            let mut sum = 0.0;
            for u in 0..8 {
                for v in 0..8 {
                    let cu = if u == 0 { 1.0 / 2.0f64.sqrt() } else { 1.0 };
                    let cv = if v == 0 { 1.0 / 2.0f64.sqrt() } else { 1.0 };
                    sum += cu * cv * block[u * 8 + v]
                         * C_MATRIX[u][x]  // cos((2x+1)uπ/16)
                         * C_MATRIX[v][y]; // cos((2y+1)vπ/16)
                }
            }
            temp[x * 8 + y] = 0.25 * sum;
        }
    }
    *block = temp;
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_fdct_idct_roundtrip() {
        let orig: [f64; 64] = core::array::from_fn(|i| {
            let row = i / 8;
            let col = i % 8;
            (row as f64 * col as f64 % 256.0) - 128.0
        });
        let mut block = orig;
        fdct_2d(&mut block);
        idct_2d_naive(&mut block);
        for i in 0..64 {
            assert!((block[i] - orig[i]).abs() < 0.1,
                "roundtrip error at {i}: got {}, expected {}", block[i], orig[i]);
        }
    }

    #[test]
    fn test_fdct_identity() {
        let mut block = [1.0f64; 64];
        fdct_2d(&mut block);
        assert!((block[0] - 8.0).abs() < 0.01, "DC value should be 8.0, got {}", block[0]);
        for i in 1..64 {
            assert!(block[i].abs() < 0.01, "AC value at {i} should be 0, got {}", block[i]);
        }
    }
}
